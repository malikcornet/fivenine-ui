// Generates tokens/tokens.css and tokens/themes/{light,dark}.css from the
// design-token sources in src/tokens/. tokens.css carries the primitives plus
// light defaults on :root; the theme files override semantics per data-theme
// and declare color-scheme via a custom format (no post-processing).

import path from 'node:path';
import { fileURLToPath } from 'node:url';
import StyleDictionary from 'style-dictionary';
import { fileHeader, formattedVariables } from 'style-dictionary/utils';

const libRoot = path.dirname(fileURLToPath(import.meta.url));

const hooks = {
  fileHeaders: {
    'fn-header': () => ['Generated by library/build-tokens.mjs — edit src/tokens/*.json instead.'],
  },
  formats: {
    // css/variables plus an optional plain `color-scheme` declaration, wrapped
    // in a `fivenine.*` cascade sublayer so unlayered consumer CSS always wins.
    'fn/css-variables': async ({ dictionary, options, file }) => {
      const header = await fileHeader({ file });
      const scheme = options.colorScheme ? `    color-scheme: ${options.colorScheme};\n` : '';
      const variables = formattedVariables({
        format: 'css',
        dictionary,
        formatting: { indentation: '    ' },
        usesDtcg: options.usesDtcg,
      });
      return `${header}@layer ${options.layer} {\n  ${options.selector} {\n${scheme}${variables}\n  }\n}\n`;
    },
  },
};

function cssBuild({ source, destination, selector, colorScheme, filter, layer }) {
  return new StyleDictionary({
    source: source.map((file) => path.join(libRoot, 'src/tokens', file)),
    hooks,
    platforms: {
      css: {
        transformGroup: 'css',
        prefix: 'fn',
        buildPath: path.join(libRoot, 'tokens/'),
        files: [
          {
            destination,
            format: 'fn/css-variables',
            filter,
            options: { selector, colorScheme, layer, fileHeader: 'fn-header' },
          },
        ],
      },
    },
    log: { verbosity: 'silent' },
  }).buildAllPlatforms();
}

const themeFilter = (theme) => (token) => token.filePath.endsWith(`${theme}.json`);

await cssBuild({
  source: ['base.json', 'light.json'],
  destination: 'tokens.css',
  selector: ':root',
  layer: 'fivenine.tokens',
});
await cssBuild({
  source: ['base.json', 'light.json'],
  destination: 'themes/light.css',
  selector: '[data-theme="light"]',
  colorScheme: 'light',
  filter: themeFilter('light'),
  layer: 'fivenine.themes',
});
await cssBuild({
  source: ['base.json', 'dark.json'],
  destination: 'themes/dark.css',
  selector: '[data-theme="dark"]',
  colorScheme: 'dark',
  filter: themeFilter('dark'),
  layer: 'fivenine.themes',
});

console.log('✓ Tokens built: tokens/tokens.css, tokens/themes/{light,dark}.css');
